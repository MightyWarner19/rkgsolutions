<b>1) Registration</b>
<p>In order to use Ralph Accounting services at any level, and to access certain information or areas of the website, a user may be required to complete a registration form. During registration a user is required to give contact information (such as name, telephone number or an email address). We use this information to contact the user about products and services on our sites. It is optional customer to provide demographic information (such as income level and gender), but encouraged so we can provide a more personalized experience on our sites.</p>
<b>2) Order</b>
<p>When a user purchases a product, we request information from the user on our order form. A user must provide contact information (such as name, email, and billing address) and financial information (such as credit card number, expiration date). This information is used for billing purposes and to fill customer’s orders. If we have trouble processing an order, the information is used to contact the user.</p>
<b>3) Profile</b>
<p>We store information that we collect through cookies and log files to create a profile of our users. A profile is stored information that we keep on individual users that details their viewing preferences. Consequently, collected information is tied to the user’s personally identifiable information to provide offers and improve the content of the site for the user. This profile is used to tailor a user’s visit to our Web sites, and to direct pertinent marketing promotions to them. We do not share personally identifiable elements of your profile with other third parties. Your profile is shared in aggregate form only.</p>
<b>4) Cookies</b>
<p>A cookie is a piece of data stored on the user’s computer tied to information about the user. Usage of a cookie is in no way linked to any personally identifiable information while on our site. We use both session ID cookies and persistent cookies. For the session ID cookie, once users close the browser, the cookie simply terminates. A persistent cookie is a small text file stored on the user’s hard drive for an extended period of time. Persistent cookies can be removed by following Internet browser help file directions.</p>
By setting a cookie on our site, users would not have to log in a password more than once, thereby saving time while on our site. If users reject the cookie, they may still use our site. The only drawback to this is that the user will be limited in some areas of our site. Persistent cookies enable us to track and target the interests of our users to enhance the experience on our site. See the “Profile” section, above.</p>
