<p>Like most Web sites, our servers use log files to analyze trends, administer the site, track user’s movement in the aggregate, and gather broad demographic information for aggregate use. IP addresses, etc. are not linked to personally identifiable information.</p>
<b>6) Clear Gifs</b>
<p>Clear gifs are tiny graphics with a unique identifier, similar in function to cookies, and are used to track the online movements of Web users. The main difference between the two is that clear gifs are invisible on the page and are much smaller, about the size of the period at the end of this sentence. Clear gifs are not tied to users’ personally identifiable information.</p>
We also use clear gifs in our HTML-based emails to let us know which emails the recipients have opened. This allows us to gauge the effectiveness of certain communications and the effectiveness of our marketing campaigns. If users would like to opt-out of these emails, please see the Opt-out section.</p>
<b>7) Children’s Privacy</b>
<p>We do not knowingly collect personal information from children under the age of 13. If we learn that we have personal information on a child under the age of 13, we will delete that information from our systems. Ralph Accounting  encourages parents to go online with their kids. Here are a few tips to help make a child’s online experience safer:</p>
Teach kids never to give personal information, unless supervised by a parent or responsible adult. Includes name, address, phone, school, etc. Know the sites your kids are visiting and which sites are appropriate. Look for Web site privacy policies. Know how your child’s information is treated.</p>
Check out the FTC’s site for more tips on protecting children’s privacy online.</p>
